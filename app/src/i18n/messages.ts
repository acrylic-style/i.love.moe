export function translate(
  locale: Locale,
  key: string,
  values: Record<string, string | number> = {},
): string {
  const template = messages[locale][key] ?? messages.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}
